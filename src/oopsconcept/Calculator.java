package oopsconcept;

public class Calculator {
	//pgm for addition

	public int sum(int a, int b) {				
		return a+b;
}
   //pgm for subtraction
	public int sub(int a, int b) {				
		return a-b;
}

//pgm for division
	public int div(int a, int b) {				
		return a/b;
}
//	pgm for remainder
	public int rem(int a, int b) {				
		return a%b;
}
//	pgm for multiplication
	public int mul(int a, int b) {				
		return a*b;
}

}
